function t(key, vars) {
    let s = (WC_T[WC_LANG] && WC_T[WC_LANG][key]) ?? WC_T.fr[key] ?? key;
    if (vars) {
        for (const [k, v] of Object.entries(vars)) {
            s = s.split(`{${k}}`).join(v === undefined || v === null ? "" : String(v));
        }
    }
    return s;
}
// Pluriel simple (fr/ar n'accordent pas pareil, ex. "1 trajet analysé" vs "3 trajets
// analysés" en français ; l'arabe utilise ici la même forme singulière+nombre que pour
// le reste de l'UI -- pas de duel/pluriel brisé grammatical, cohérent avec le choix déjà
// fait ailleurs (ex. "{n} تذكرة") de garder l'arabe simple plutôt que grammaticalement
// exhaustif). `n` détermine juste FR singulier/pluriel ; la clé `_n` sert aussi de forme
// arabe par défaut quand n===1.
function tPlural(base, n, vars = {}) {
    const key = n === 1 ? `${base}_n` : `${base}_p`;
    return t(key, { n, ...vars });
}
